using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Utils
{
    public class utils
    {
        public static void add_numbers(int a, int b)
        {
            int c = a + b;
            Console.WriteLine(a+"+"+b+"="+c);
        }
        public static void multiply_numbers(int a, int b)
        {
            int c = a * b;
            Console.WriteLine(a + "*" + b + "=" + c);
        }
    }
}
